# 2) To print whether number is positive , negative , or zero using if-else statements.

num = int(input('Enter a Number : '))
if num > 0:
    print(num, "is a positive number")
elif num < 0:
     print(num, "is a negative number")
else:
     print(num, "is zero")

# ----------Output---------
# Enter a Number : 4
# 4 is a positive number

# Enter a Number : 0
# 0 is zero

# Enter a Number : -2
# -2 is a negative number
